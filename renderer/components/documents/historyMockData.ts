import { Document, DocumentOptions } from '../../types'

const defaultOptions: DocumentOptions = {
  includeTax: true,
  reducedTaxRate: false,
  withholdingTax: false,
  showRemarks: false,
  showBankInfo: true,
}

const build = (
  overrides: Partial<Document> & Pick<Document, 'id' | 'documentType' | 'documentNumber' | 'issueDate' | 'clientId' | 'clientName' | 'totalAmount'>
): Document => ({
  subtotal: Math.round(overrides.totalAmount / 1.1),
  taxAmount: overrides.totalAmount - Math.round(overrides.totalAmount / 1.1),
  withholdingTax: 0,
  options: defaultOptions,
  stampId: null,
  detailMode: 'direct',
  remarks: null,
  pdfFilePath: null,
  createdAt: `${overrides.issueDate}T09:00:00Z`,
  updatedAt: `${overrides.issueDate}T09:00:00Z`,
  ...overrides,
})

